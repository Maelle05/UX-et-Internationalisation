                <p id="blanc_text">Contrairement au reste du monde, <br>
                     en Chine, le blanc ne signifie pas la pureté, <br>
                     l’élégance ou encore la neutralité <br>
                     mais la mort et le deuil.
                </p>
                <p id="noir_text">En Europe et en Amérique, <br>
                     le noir symbolise la mort, <br>
                      le diable, le deuil, <br>
                       alors qu’en Afrique il symbolise la masculinité <br>
                        et la maturité et en Asie <br>
                         la renaissance en plus du deuil.
                        </p>
                <p id="vert_text">En Europe, le vert représente la chance, <br>
                     aux Etats-Unis la jalousie,<br> au Mexique l’indépendance <br>
                     et Asie la jeunesse et la fertilité. <br>
                     En Indonésie, cette couleur <br>
                     est traditionnellement interdite.
                </p>
                <p id="bleu_text">Le bleu représente <br>
                     pour la plupart des pays <br>
                      la sérénité et le calme, <br>
                       alors qu’en Afrique il représente <br>
                        la guérison et en Asie l’immortalité.
                </p>
                <p id="rouge_text">En Amérique du Sud et en Afrique,<br>
                     le rouge symbolise le deuil et la mort, <br>
                      alors qu’en Asie,<br>
                      il signifie la bonne fortune, <br>
                       la joie et la prospérité.
                </p>
                <p id="jaune_text">Le jaune signifie la royauté <br>
                     et l’impérialisme en Asie, <br>
                      ainsi que le courage au Japon. <br>
                      En Europe et en Afrique,<br>
                       il symbolise la joie et la bonne humeur. <br>
                        Aux Etats-Unis, le jaune symbolise <br>
                         l’idée de mouvement.
                </p>
                <p id="orange_text">En Europe, l’orange représente la chaleur, <br>
                    la moisson. En Amérique du nord l’abondance, <br>
                     en Colombie la fertilité et <br>
                      en Afrique et en Asie la joie.
                </p>
                <p id="violet_text"> Le violet symbolise la royauté, <br>
                 la richesse et la noblesse dans l’ensemble du monde excepté <br>
                  en Thaïlande et au Brésil, où il signifie le deuil.
                </p>
